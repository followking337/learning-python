print('\n// and % (-):')
print(19 / 5)
print(19 // 5)
print(19 % 5)
print(19 / -5)
print(-19 / 5)

print('\nДеление нацело негативных чисел:')  # округляется вниз
print(19 // -5)
print(-19 // 5)

print('\nДеление по остатку негативных чисел:')
print(19 % -5)
print(-19 % 5)

print('\nДеление нацело и деление по остатку негативных чисел оба негативных:')
print(-19 / -5)
print(-19 // -5)
print(-19 % -5)

print('\nПример:')
print(-33 / 7)
print(-33 // 7)
print(-33 % 7)
